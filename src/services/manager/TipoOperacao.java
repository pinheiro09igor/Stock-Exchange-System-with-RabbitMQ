package services.manager;

/**
 * Enumeração que define os tipos de operações em um mercado financeiro.
 * As operações podem ser de compra ou venda de ativos.
 */
public enum TipoOperacao {
    /**
     * Representa uma operação de compra de ativos.
     */
    COMPRA,

    /**
     * Representa uma operação de venda de ativos.
     */
    VENDA
}
